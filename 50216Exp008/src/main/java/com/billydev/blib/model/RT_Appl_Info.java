package com.billydev.blib.model;

public class RT_Appl_Info {

	private String application_name;
	private String state; 
	private long generationNumber; 
	private long appl_id;
	public String getApplication_name() {
		return application_name;
	}
	public void setApplication_name(String application_name) {
		this.application_name = application_name;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public long getGenerationNumber() {
		return generationNumber;
	}
	public void setGenerationNumber(long generationNumber) {
		this.generationNumber = generationNumber;
	}
	public long getAppl_id() {
		return appl_id;
	}
	public void setAppl_id(long appl_id) {
		this.appl_id = appl_id;
	} 
	
	
	
}
